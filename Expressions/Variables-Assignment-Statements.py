# Variable assignment
x = 12.2
y = 14

print(x) # Output: 12.2
print(y) # Output: 14

# Reassigning a variable
x = 100
print(x) # Output: 100


# Reassigning variables
z = 0.6
z = z * 1.56

print(z) # Output: 0.936

# Constants and variables
x = 5
y = 10
z = x + y

print("The sum of", x, "and", y, "is", z) # Output: The sum of 5 and 10 is 15
