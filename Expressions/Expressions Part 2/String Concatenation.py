# Concatenating strings
str1 = "Hello"
str2 = "World"
result = str1 + " " + str2
print(result)  # Output: Hello World

# Concatenating integers as strings
int1 = 123
int2 = 456
result = str(int1) + str(int2)
print(result)  # Output: 123456


# Integer constant
i = 123
print(i)  # Output: 123

# Floating point constant
f = 98.6
print(f)  # Output: 98.6

# String constant
s = "Hello world"
print(s)  # Output: Hello world


# Integer division
x = 9 // 2
print(x)  # Output: 4

# Floating point division
y = 9 / 2
print(y)  # Output: 4.5
